    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
         try{
            con=Javaconnect.connectDB();
            JasperDesign jd=net.sf.jasperreports.engine.xml.JRXmlLoader.load("src//visitors.jrxml");
            
            JasperReport jp=JasperCompileManager.compileReport(jd);
            JasperPrint jpr=JasperFillManager.fillReport(jp, null, con);
            JRViewer jv=new JRViewer(jpr);
            JFrame f1=new JFrame();
            f1.add(jv);
            f1.setTitle("Visitors records");
            f1.setVisible(true);
            jv.setVisible(true);
            //jv.setSize(600,400);
            jv.setOpaque(true);
            f1.repaint();
            f1.validate();
            f1.setSize(new Dimension(742, 551));
            f1.setLocation(400, 140);
            f1.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
            
          
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed


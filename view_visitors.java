    public void filltable1(){
        model1.setRowCount(0);
        String sql="select * from visitors";
        
    try {
        st=con.createStatement();
        rs=st.executeQuery(sql);
        while(rs.next()){
            model1.addRow(new Object[]{rs.getString("id"),rs.getString("firstname"),rs.getString("lastname_in"),rs.getString("mobile"),rs.getString("email"),rs.getString("Location"),rs.getString("address"),rs.getString("purpose"),rs.getString("time_in")});
        }
    } catch (SQLException ex) {
        Logger.getLogger(view_visitors.class.getName()).log(Level.SEVERE, null, ex);
    }
        
    }
    public void filltable2(){
        model2.setRowCount(0);
        String sql="select * from visitors where id=?";
       if(search.getText().equals(""))
           JOptionPane.showMessageDialog(this, "Please enter Id number");
       else{
    try {
         ps=con.prepareStatement(sql);
         ps.setString(1, search.getText());
         rs=ps.executeQuery();
         if(rs.next()){
          
         JOptionPane.showMessageDialog(this, "visitor "+rs.getString("Firstname")+ " exists");
          model2.addRow(new Object[]{rs.getString("id"),rs.getString("firstname"),rs.getString("lastname_in"),rs.getString("mobile"),rs.getString("email"),rs.getString("Location"),rs.getString("address"),rs.getString("purpose"),rs.getString("time_in")});
         }
          else {
              JOptionPane.showMessageDialog(this, "user does exist");
          }
    } catch (SQLException ex) {
        Logger.getLogger(view_visitors.class.getName()).log(Level.SEVERE, null, ex);
    }
    }
    }
